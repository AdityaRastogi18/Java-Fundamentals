package Java;

public class Operators {

	public static void main(String[] args) {
		
		int num = 180;
		
		if((num == 180) && (num%2 == 0)) {
			
			System.out.println("Number is Even & true");
			
		}
		
		int num1 = 155;
		
		if((num1 == 154) || (num1%2 == 1)) {
			
			System.out.println("Number is Odd & false");
			
		}

	}

}
